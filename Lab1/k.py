# a > b
a = 50
b = 10
if a > b:
    print("Hello World")


# !=
a = 50
b = 10
if a != b :
    print("Hello World")


#else
a = 50
b = 10
if a == b:
    print("Yes")
else:
    print("No")


#elif
a = 50
b = 10
if a == b:
    print("1")
elif a > b:
    print("2")
else:
    print("3")


#and
if a == b and c == d:
    print("Hello")


#or
if a == b or c == d:
    print("Hello")


