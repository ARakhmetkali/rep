"""
TUPLE
"""
#1. print the first item
fruits = ("apple", "banana", "cherry")
print(fruits[0])


#2. number of items
fruits = ("apple", "banana", "cherry")
print(len(fruits))


#3. negative index to print the last item in the tuple
fruits = ("apple", "banana", "cherry")
print(fruits[-1])


#4. range of indexes to print the third, fourth, and fifth item in the tuple
fruits = ("apple", "banana", "cherry", "orange", "kiwi", "melon", "mango")
print(fruits[2:5])